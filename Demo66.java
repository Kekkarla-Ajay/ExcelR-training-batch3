public class Demo066 {
    public static void main(String[] args) {
        // Initialize an array with some values
        int[] arr = {10, 20, 30, 40, 50};

        // Print the index and corresponding element of the array
        System.out.println("Array indices and their corresponding elements:");
        for (int i = 0; i < arr.length; i++) {
            System.out.println("Index: " + i + " -> Element: " + arr[i]);
        }
    }
}